// Operadores aritméticos de atribuição e subritração
let a = 10;
let b = 5;

// Soma
const soma = a + b; // 15

// Subtração
const subtracao = a - b; // 5

// Multiplicação
const multiplicacao = a * b; // 50

// Divisão
const divisao = a / b; // 2

// Módulo (resto da divisão)
const modulo = a % b; // 0

// Exponenciação
const exponenciacao = a ** b; // 100000

// Incremento
let incremento = a + 1; // 11
let incrementoPre = a + 2; // 12

// Decremento
let decremento = a - 1; // 9
let decrementoPre = a - 2; // 8